<h2>Punktacja</h2>
<div>
	<p>
		Za każde piwo własne, a więc uwarzone pod własną marką, przyznawane są punkty w zależności od
		jego średniej oceny z portali RateBeer oraz Untappd. Ilość punktów zależy od odchylenia jego
		średniej oceny od średniej oceny dla wszystkich piw ocenionych w bazie danych, tj. obecnie około
		3,61. Jeden punkt odpowiada 0,1 punktu odchylenia. Jeżeli więc piwo ma ocenę 4,26, a więc jest
		ocenione o 0,65 wyżej niż średnia wszystkich piw, piwo dostaje 7 punktów. I w drugą stronę,
		jeżeli piwo ma ocenę 3,4, a więc jest ocenione o 0,21 niżej, piwo dostaje -3 punkty.
	</p>
	<p>
		Nie jestem przekonany co do sprawiedliwości tej oceny i w ogóle ocen pochodzących z RateBeer czy
		Untappd. Mocne, aromatyczne RISy mają z reguły wyższe oceny niż codzienne schwarzbiery, portery,
		pilsy czy ogólnie piwa nisko-alkoholowe, choćby były najwyższej jakości.
	</p>
</div>

